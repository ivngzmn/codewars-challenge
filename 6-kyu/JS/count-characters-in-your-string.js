function count(string) {
    let hash = {};
    string.split('').map((letter) => (hash[letter] = hash[letter] + 1 || 1));
    return hash;
}

console.log(count('aba'), { a: 2, b: 1 });
console.log(count('abccbbaaabc'), { a: 4, b: 4, c: 3 });
console.log(count(''), {});
