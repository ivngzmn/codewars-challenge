function abbrevName(name) {
    return name
        .split(' ')
        .map((firstLastIn) => firstLastIn.slice(0, 1).toUpperCase()) // substr is deprecated
        .join('.');
}

console.log(abbrevName('Sam Harris'));
console.log(abbrevName('patrick feeney'));
